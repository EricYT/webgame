-module(cache_processor_sup).

-behaviour(supervisor).
%% --------------------------------------------------------------------
%% Include files
%% --------------------------------------------------------------------

%% --------------------------------------------------------------------
%% External exports
%% --------------------------------------------------------------------
-export([start_link/3]).

%% --------------------------------------------------------------------
%% Internal exports
%% --------------------------------------------------------------------
-export([
	 init/1
        ]).

%% --------------------------------------------------------------------
%% Macros
%% --------------------------------------------------------------------
-define(SERVER, ?MODULE).

%% --------------------------------------------------------------------
%% Records
%% --------------------------------------------------------------------

%% ====================================================================
%% External functions
%% ====================================================================
start_link(CacheProcessor, TableName, TimeRefresh) ->
	ServerName = cache_util:make_cache_sup_name(CacheProcessor),
	supervisor:start_link({local, ServerName}, ?SERVER, [CacheProcessor, TableName, TimeRefresh]).


%% ====================================================================
%% Server functions
%% ====================================================================
%% --------------------------------------------------------------------
%% Func: init/1
%% Returns: {ok,  {SupFlags,  [ChildSpec]}} |
%%          ignore                          |
%%          {error, Reason}
%% --------------------------------------------------------------------
init([CacheProcessor, TableName, TimeRefresh]) ->
    AChild = {'cache_processor',{'cache_processor',start_link,[CacheProcessor, TableName, TimeRefresh]},
	      permanent,2000,worker,['cache_processor']},
    {ok,{{one_for_one,0,1}, [AChild]}}.

